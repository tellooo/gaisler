#include <sys/times.h>
#include "bcc/bcc.h"

/*
 * NOTE: Time units for times() is not the same as for clock()
 * (CLOCKS_PER_SEC).
 */
clock_t times(struct tms *buf)
{
        clock_t t;

        t = bcc_timer_get_us();
        buf->tms_utime = t;
        buf->tms_stime = 0;
        buf->tms_cutime = 0;
        buf->tms_cstime = 0;

        return t;
}

